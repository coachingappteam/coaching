class Athlete {
  final int id;
  final String firstName;
  final String lastName;
  final String phone;
  final String email;
  final String height;
  final double weight;
  final String birthDate;
  final String sex;

  const Athlete({
    this.id,
    this.firstName,
    this.lastName,
    this.phone,
    this.email,
    this.height,
    this.weight,
    this.birthDate,
    this.sex,
  });
}
